console.log(3 * (10 - 4));

// Constant expression
3;
('Hello');

// Assignment expression
result = 3 * (10 - 4);

// lookup expression
result;

/**
 * Mutability - the value can change
 * Immutability - the value cannot change
 */
// let keyword - mutable
let age = 20;

age;

age = 21;

console.log(age);

// const keyword - immutable
const name = 'Nelson';

console.log(name);

// var keyword - never use it

var fullName = 'John Doe';

fullName;

var fullName = 'Jane Doe';

console.log(fullName);
